print("Days of the week")

dia = int(input("Enter a number: "))

if dia == 1:
    print("Monday")
elif dia == 2:
    print("Tuesday")
elif dia == 3:
    print("Wednesday")
elif dia == 4:
    print("Thursday")
elif dia == 5:
    print("Friday")
elif dia == 6:
    print("Saturday")
elif dia == 7:
    print("Sunday")
else:
    print("Invalid number")